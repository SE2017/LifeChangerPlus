package se2017.lex;

/**
 * This is an object that contains data about the HealthIndex.
 */


public class IndexObjects {
    public int currentHI;
    public int LITime;
    public int HITime;

    public IndexObjects(int chi, int lit, int hit)
    {
        this.currentHI = chi;
        this.LITime = lit;
        this.HITime = hit;
    }

}
